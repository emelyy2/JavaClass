package ClassesAndObjects;

public class Human {

        String name;

        public void eat(){
            System.out.println(name + " is eating");
        }

        public void speak(){
            System.out.println(name + " is speaking");
        }

        public void walk(){
            System.out.println(name + " is walking");
        }
        public void talk(){
            System.out.println(name + "is talking");
        }
}
