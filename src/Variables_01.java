public class Variables_01 {

    //main method shortcuts
    // 1. psvm (enter)
    // 2. main (enter)

    public static void main(String[] args) {

        //All Java code goes inside the main method

        //console shorcouts
        // sout (enter)
        System.out.println("Hello World!");

        //Variables
        int age; //variable declaration
        age= 25; //variable initialization
        System.out.println(age);

        int number = 30; //declaration + initialization
        System.out.println(number);

        //String

        String text = "Hello";
        System.out.println(text);

        int num = 200;
        System.out.println(num);

        //Adding 2 or more values together
        int x = 40;
        int y = 20;
        int total= x + y;
        System.out.println(total);

        //change values
        x = 20;
        y = 100;
        total = x + y;
        System.out.println(total);


        // Concatenation
        String bornYear = "1980";
        System.out.println("I was born in " + bornYear);

        int myAge = 27;
        String myName = "Jhon";
        System.out.println("My name is " + myName + ", "+ "and I am " + myAge);





    }
}
